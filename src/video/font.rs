/// The 8x8 bitmap for `ch`, or the blank cell (space) for anything
/// outside the printable ASCII range.
pub fn glyph(ch: char) -> &'static [u8; 8] {
    let c = ch as usize;
    if (FIRST..=LAST).contains(&c) {
        &FONT[c - FIRST]
    } else {
        &FONT[0]
    }
}

/// Device-pixel width of `text` rendered at `px` device pixels per font
/// pixel. Each glyph cell is [`GLYPH_W`] font pixels wide (the cell
/// already includes inter-character spacing).
pub fn text_width(text: &str, px: usize) -> usize {
    text.chars().count() * GLYPH_W * px
}

/// Device-pixel height of one text row at `px` device pixels per font
/// pixel.
pub fn text_height(px: usize) -> usize {
    GLYPH_H * px
}

/// Blit `text` into an RGBA8 `frame` of `frame_w` x `frame_h` device
/// pixels, with the top-left of the first glyph at (`x`, `y`). Each font
/// pixel is drawn as a `px`-by-`px` block in `color` (packed 0xAABBGGRR,
/// i.e. R,G,B,A in memory order). Pixels outside the frame are clipped.
/// Returns the x just past the last glyph.
#[allow(clippy::too_many_arguments)]
pub fn draw_text(
    frame: &mut [u8],
    frame_w: usize,
    frame_h: usize,
    x: usize,
    y: usize,
    text: &str,
    color: u32,
    px: usize,
) -> usize {
    let bytes = color.to_le_bytes();
    let mut cursor_x = x;
    for ch in text.chars() {
        let g = glyph(ch);
        for (row_idx, row) in g.iter().enumerate() {
            for col in 0..GLYPH_W {
                if row & (1 << col) == 0 {
                    continue;
                }
                let bx = cursor_x + col * px;
                let by = y + row_idx * px;
                for dy in 0..px {
                    let fy = by + dy;
                    if fy >= frame_h {
                        break;
                    }
                    for dx in 0..px {
                        let fx = bx + dx;
                        if fx >= frame_w {
                            break;
                        }
                        let off = (fy * frame_w + fx) * 4;
                        frame[off..off + 4].copy_from_slice(&bytes);
                    }
                }
            }
        }
        cursor_x += GLYPH_W * px;
    }
    cursor_x
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn printable_range_is_complete() {
        assert_eq!(FONT.len(), 95);
        // Space is blank, 'A' is not.
        assert_eq!(glyph(' '), &[0u8; 8]);
        assert_ne!(glyph('A'), &[0u8; 8]);
        // Out-of-range falls back to the blank cell.
        assert_eq!(glyph('\u{2014}'), &[0u8; 8]);
    }

    #[test]
    fn text_metrics_scale_with_pixel_size() {
        assert_eq!(text_width("AB", 2), 2 * GLYPH_W * 2);
        assert_eq!(text_height(3), GLYPH_H * 3);
    }

    #[test]
    fn draw_text_sets_and_clips_pixels() {
        let (w, h) = (64usize, 16usize);
        let mut frame = vec![0u8; w * h * 4];
        // 'I' (0x49) has a set pixel; draw at origin, 1px scale.
        draw_text(&mut frame, w, h, 0, 0, "I", 0xFFFFFFFF, 1);
        // At least one white pixel was written.
        assert!(frame.chunks_exact(4).any(|p| p == [0xFF, 0xFF, 0xFF, 0xFF]));
        // Drawing partly off the right edge must not panic.
        draw_text(&mut frame, w, h, w - 2, 0, "WIDE", 0xFFFFFFFF, 2);
    }
}
